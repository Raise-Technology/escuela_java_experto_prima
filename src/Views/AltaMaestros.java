    public void enviarMaestro(){
        maestro.setNombre(txtNombre.getText());
        maestro.setDireccion(txtDireccion.getText());
        maestro.setCorreo(txtCorreo.getText());
        maestro.setEdad((int) spnEdad.getValue());
        maestro.setTelefono(txtTelefono.getText());
        maestro.setPais(cbxPais.getSelectedItem().toString());
        if(rbtMujer.isSelected()){
            maestro.setSexo(rbtMujer.getText());
        }
        if(rbtHombre.isSelected()){
            maestro.setSexo(rbtHombre.getText());
        }
        
        if(maestro.guardarMaestro(maestro)){
            JOptionPane.showMessageDialog(this, "Guardado Correctamente");
        }else{
            JOptionPane.showMessageDialog(this, "Error al guardar");
        }

    }
    
